public class Atividade2 {
    public static void main(String[] args) {
        System.out.println("10 == 10 && 10 == 20 --> " + (10 == 10 && 10 == 20));
        System.out.println("11 == 12 && 20 == 20 --> " + (11 == 12 && 20 == 20));
        System.out.println("15 == 15 && 16 == 16 --> " + (15 == 15 && 16 == 16));
        System.out.println("12 == 11 && 23 == 24 --> " + (12 == 11 && 23 == 24));
        System.out.println("10 == 10 || 10 == 20 --> " + (10 == 10 || 10 == 20));
        System.out.println("11 == 12 || 20 == 20 --> " + (11 == 12 || 20 == 20));
        System.out.println("15 == 15 || 16 == 16 --> " + (15 == 15 || 16 == 16));
        System.out.println("12 == 11 || 23 == 24 --> " + (12 == 11 || 23 == 24));             
    }    
}
