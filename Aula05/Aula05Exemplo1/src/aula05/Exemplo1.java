package aula05;
public class Exemplo1 {
    public static void main(String[] args) {
        double A, B, C;
        double x = 9.0;
        double y = 4.0;
        double z = -8.0;
        
        A = Math.sqrt(x);
        B = Math.sqrt(y);
        C = Math.sqrt(z);
        
        System.out.println("A raiz quadrada de " + x + " = " + A);
        System.out.println("A raiz quadrada de " + y + " = " + B);
        System.out.println("A raiz quadrada de " + z + " = " + C);                
    }    
}
