package Aula28;
public class SimpleClass {

    public int addEm(int a, int b) {
        return a + b;
    }
    public int subEm(int a, int b) {
        return a - b;
    }
    public float divEm(int a, int b) {
        float resp = a / b;
        return resp;
    }    
    public float MulEm(int a, int b) {
        float resp = a * b;
        return resp;
    }
}
